function makeSaleItem(
  productId: string,
  productName: string,
  quantity: number,
  rate: number,
  hsnCode = "1508",
  gstPercent = 5,
  discount = 0,
): Sale["items"][number] {
  const baseAmount = quantity * rate - discount;
  const gstAmount = (baseAmount * gstPercent) / 100;
  return {
    productId,
    productName,
    quantity,
    rate,
    amount: baseAmount + gstAmount,
    discount,
    gstPercent,
    hsnCode,
    gstAmount,
  };
}

