const now = new Date();

function dateStr(daysAgo: number): string {
  return format(subDays(now, daysAgo), "yyyy-MM-dd");
}

function monthStr(monthsAgo: number, day: number): string {
  const d = subMonths(now, monthsAgo);
  d.setDate(day);
  return format(d, "yyyy-MM-dd");
}

